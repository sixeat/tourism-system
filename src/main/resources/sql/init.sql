insert into route_spot (route_id, scenic_id, day_no, sort_no)
select tr.id, s.id,
       case when ranked.rn <= 2 then 1 when ranked.rn <= 4 then 2 else 3 end,
       case when ranked.rn in (1,3,5) then 1 else 2 end
from travel_route tr
join (
    select s.*,
           row_number() over (partition by s.city order by s.popularity desc, s.score desc, s.id asc) rn
    from scenic_spot s
    where s.city in ('上海','成都','西安','厦门','桂林','重庆')
) ranked on ranked.city = tr.city and ranked.rn <= case when tr.days = 2 then 4 else 5 end
join scenic_spot s on s.id = ranked.id
where tr.city in ('上海','成都','西安','厦门','桂林','重庆');

