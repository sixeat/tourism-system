create database if not exists tourism_system default character set utf8mb4;
use tourism_system;

create table if not exists scenic_spot (
    id bigint primary key auto_increment,
    scenic_name varchar(100) not null,
    city varchar(50) not null,
    category varchar(50),
    description text,
    price decimal(10, 2),
    score decimal(3, 1),
    popularity int,
    tags varchar(255),
    longitude decimal(10, 6),
    latitude decimal(10, 6)
);

create table if not exists sys_user (
    id bigint primary key auto_increment,
    username varchar(50) not null unique,
    password varchar(100) not null,
    phone varchar(20),
    email varchar(100),
    role varchar(20) default 'USER',
    status int default 1,
    create_time datetime default current_timestamp
);

create table if not exists hotel (
    id bigint primary key auto_increment,
    hotel_name varchar(100) not null,
    city varchar(50) not null,
    address varchar(255),
    level varchar(20),
    description text,
    status int default 1
);

create table if not exists hotel_room (
    id bigint primary key auto_increment,
    hotel_id bigint not null,
    room_type varchar(50) not null,
    price decimal(10, 2) not null,
    stock int default 0,
    room_status varchar(20) default 'AVAILABLE'
);

create table if not exists ticket (
    id bigint primary key auto_increment,
    scenic_id bigint not null,
    ticket_name varchar(100) not null,
    price decimal(10, 2) not null,
    stock int default 0,
    available_date date
);

create table if not exists travel_route (
    id bigint primary key auto_increment,
    route_name varchar(100) not null,
    city varchar(50) not null,
    days int not null,
    budget decimal(10, 2),
    theme varchar(50),
    route_desc text
);

create table if not exists route_spot (
    id bigint primary key auto_increment,
    route_id bigint not null,
    scenic_id bigint not null,
    day_no int not null,
    sort_no int not null
);

create table if not exists hotel_order (
    id bigint primary key auto_increment,
    user_id bigint not null,
    hotel_id bigint not null,
    room_id bigint not null,
    check_in_date date not null,
    check_out_date date not null,
    total_amount decimal(10, 2) not null,
    order_status varchar(20) default 'CREATED',
    create_time datetime default current_timestamp
);

create table if not exists ticket_order (
    id bigint primary key auto_increment,
    user_id bigint not null,
    ticket_id bigint not null,
    visit_date date not null,
    quantity int not null,
    total_amount decimal(10, 2) not null,
    order_status varchar(20) default 'CREATED',
    create_time datetime default current_timestamp
);

create table if not exists user_favorite (
    id bigint primary key auto_increment,
    user_id bigint not null,
    scenic_id bigint,
    favorite_type varchar(20) default 'SCENIC',
    target_id varchar(100),
    title varchar(100),
    description varchar(255),
    create_time datetime default current_timestamp,
    unique key uk_user_favorite_target (user_id, favorite_type, target_id)
);

create table if not exists order_review (
    id bigint primary key auto_increment,
    user_id bigint not null,
    order_type varchar(20) not null,
    order_id bigint not null,
    score int not null,
    content varchar(500) not null,
    create_time datetime default current_timestamp,
    unique key uk_order_review_user_order (user_id, order_type, order_id)
);

create table if not exists map_point (
    id bigint primary key auto_increment,
    point_name varchar(100) not null,
    point_type varchar(30) not null,
    city varchar(50) not null,
    address varchar(255),
    description varchar(500),
    price decimal(10, 2),
    score decimal(3, 1),
    tags varchar(255),
    longitude decimal(10, 6),
    latitude decimal(10, 6),
    sort_no int default 0,
    status int default 1
);

