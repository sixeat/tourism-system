--       实现"地图点位录入一次，全业务表自动同步"的闭环数据生成逻辑。
--  关键特性：
--  1. 使用 NOT EXISTS 子句确保幂等性，重复执行不会插入重复数据；
--  2. 使用 CASE 表达式根据 tags 自动推断分类和级别，减少人工干预；
--  3. 使用 ROW_NUMBER() 窗口函数为每个城市的热门景点排序，自动分配路线日程。
-- ============================================================

-- 设置当前会话字符集为 utf8mb4，确保后续插入的中文不乱码。
set names utf8mb4;

-- ------------------------------------------------------------
--  步骤1：从 map_point 景点生成 scenic_spot 记录
--  逻辑：筛选 map_point 中 point_type='景点' 且 sort_no 在扩展区间的数据，
--       根据 tags 内容自动推断 category（城市夜游/滨海度假/人文古迹/亲子乐园/城市地标），
--       避免已存在同名同城的景点（NOT EXISTS 幂等）。
-- ------------------------------------------------------------
insert into scenic_spot (scenic_name, city, category, description, price, score, popularity, tags, longitude, latitude)
select point_name, city,
       case
           when tags like '%夜游%' then '城市夜游'
           when tags like '%海岛%' or tags like '%海滨%' then '滨海度假'
           when tags like '%历史%' or tags like '%文化%' then '人文古迹'
           when tags like '%亲子%' then '亲子乐园'
           else '城市地标'
       end,
       description, price, score, 88, tags, longitude, latitude
from map_point mp
where mp.point_type = '景点'
  and mp.sort_no between 100 and 299
  and not exists (select 1 from scenic_spot s where s.scenic_name = mp.point_name and s.city = mp.city);

-- ------------------------------------------------------------
--  步骤2：从 map_point 酒店生成 hotel 记录
--  逻辑：筛选 map_point 中 point_type='酒店' 且 sort_no 在扩展区间的数据，
--       根据 tags 内容自动推断 level（五星级/精品民宿/四星级），
--       避免已存在同名同城的酒店（NOT EXISTS 幂等）。
-- ------------------------------------------------------------
insert into hotel (hotel_name, city, address, level, description, status)
select point_name, city, address,
       case
           when tags like '%五星%' or tags like '%度假%' then '五星级'
           when tags like '%民宿%' then '精品民宿'
           else '四星级'
       end,
       description, 1
from map_point mp
where mp.point_type = '酒店'
  and mp.sort_no between 100 and 299
  and not exists (select 1 from hotel h where h.hotel_name = mp.point_name and h.city = mp.city);

-- ------------------------------------------------------------
--  步骤3：为扩展酒店生成默认房型（舒适大床房）
--  逻辑：为新插入的扩展酒店生成一种默认房型，
--       价格取 map_point 中该酒店的价格与 268.00 的较大值，保底不低于 268 元，
--       默认库存 10 间，房态为 AVAILABLE。
-- ------------------------------------------------------------
insert into hotel_room (hotel_id, room_type, price, stock, room_status)
select h.id, '舒适大床房', greatest(268.00, coalesce(mp.price, 398.00)), 10, 'AVAILABLE'
from hotel h
join map_point mp on mp.point_name = h.hotel_name and mp.city = h.city and mp.point_type = '酒店'
where mp.sort_no between 100 and 299
  and not exists (select 1 from hotel_room r where r.hotel_id = h.id and r.room_type = '舒适大床房');

-- ------------------------------------------------------------
--  步骤4：为扩展酒店生成第二种默认房型（景观双床房）
--  逻辑：在舒适大床房基础上加价 160 元，提供更高档的房型选择，
--       默认库存 6 间，丰富用户的预订选择。
-- ------------------------------------------------------------
insert into hotel_room (hotel_id, room_type, price, stock, room_status)
select h.id, '景观双床房', greatest(328.00, coalesce(mp.price, 398.00) + 160.00), 6, 'AVAILABLE'
from hotel h
join map_point mp on mp.point_name = h.hotel_name and mp.city = h.city and mp.point_type = '酒店'
where mp.sort_no between 100 and 299
  and not exists (select 1 from hotel_room r where r.hotel_id = h.id and r.room_type = '景观双床房');

-- ------------------------------------------------------------
--  步骤5：为扩展城市景点生成门票（ticket）
--  逻辑：为上海、成都、西安、厦门、桂林、重庆 6 个扩展城市的每个景点生成一张成人票，
--       门票价格取景点参考价格（若为 0 则取 0），默认库存 120 张，可用日期设为 2026-06-20。
-- ------------------------------------------------------------
insert into ticket (scenic_id, ticket_name, price, stock, available_date)
select s.id, concat(s.scenic_name, '成人票'), greatest(0.00, coalesce(s.price, 0.00)), 120, '2026-06-20'
from scenic_spot s
where s.city in ('上海','成都','西安','厦门','桂林','重庆')
  and not exists (select 1 from ticket t where t.scenic_id = s.id);

-- ------------------------------------------------------------
--  步骤6：为扩展城市生成"三日精选游"路线
--  逻辑：为 6 个扩展城市各生成一条 3 天精华路线，
--       预算按城市差异化设定（上海最高 1588，重庆最低 1188），
--       主题统一为"城市精华"，描述突出城市特色。
-- ------------------------------------------------------------
insert into travel_route (route_name, city, days, budget, theme, route_desc)
select concat(city, '三日精选游'), city, 3,
       case city
           when '上海' then 1588.00
           when '成都' then 1288.00
           when '西安' then 1388.00
           when '厦门' then 1488.00
           when '桂林' then 1288.00
           when '重庆' then 1188.00
           else 1288.00
       end,
       '城市精华', concat('串联', city, '核心景点、精选酒店和热门门票，适合期末项目完整演示。')
from (select '上海' city union all select '成都' union all select '西安' union all select '厦门' union all select '桂林' union all select '重庆') c
where not exists (select 1 from travel_route r where r.route_name = concat(c.city, '三日精选游'));

-- ------------------------------------------------------------
--  步骤7：为扩展城市生成"轻预算周末游"路线
--  逻辑：为 6 个扩展城市各生成一条 2 天轻预算路线，
--       预算低于对应的三日精选游，面向学生/轻预算群体，
--       主题统一为"经济实惠"，描述突出免费景点和低成本体验。
-- ------------------------------------------------------------
insert into travel_route (route_name, city, days, budget, theme, route_desc)
select concat(city, '轻预算周末游'), city, 2,
       case city
           when '上海' then 999.00
           when '成都' then 788.00
           when '西安' then 888.00
           when '厦门' then 968.00
           when '桂林' then 858.00
           when '重庆' then 788.00
           else 888.00
       end,
       '经济实惠', concat('面向轻预算用户，优先选择', city, '免费街区、小吃和交通便利酒店。')
from (select '上海' city union all select '成都' union all select '西安' union all select '厦门' union all select '桂林' union all select '重庆') c
where not exists (select 1 from travel_route r where r.route_name = concat(c.city, '轻预算周末游'));

-- ------------------------------------------------------------
--  步骤8：清理扩展城市旧路线关联（幂等准备）
--  逻辑：先删除扩展城市（上海、成都、西安、厦门、桂林、重庆）在 route_spot 中的旧关联，
--       避免重新执行时产生重复数据。JOIN travel_route 确保只删除属于这些城市的路线关联。
-- ------------------------------------------------------------
delete rs from route_spot rs
join travel_route tr on tr.id = rs.route_id
where tr.city in ('上海','成都','西安','厦门','桂林','重庆');

-- ------------------------------------------------------------
--  步骤9：为扩展城市路线重新生成景点关联（route_spot）
--  逻辑：使用 ROW_NUMBER() 窗口函数按城市维度对景点按 popularity 和 score 排序，
--       取每个城市热度最高的前 4（2 日游）或前 5（3 日游）个景点，
--       自动分配到第 1 天、第 2 天、第 3 天，并设定每天的游览顺序（sort_no）。
--  分配规则：
--  - rn <= 2 的景点 -> day_no = 1
--  - rn <= 4 的景点 -> day_no = 2
--  - rn > 4 的景点 -> day_no = 3
--  - rn 为奇数 -> sort_no = 1（当天第1个景点）
--  - rn 为偶数 -> sort_no = 2（当天第2个景点）
-- ------------------------------------------------------------
insert into route_spot (route_id, scenic_id, day_no, sort_no)
select tr.id, s.id,
       case when ranked.rn <= 2 then 1 when ranked.rn <= 4 then 2 else 3 end,  -- 按排名分配到第几天
       case when ranked.rn in (1,3,5) then 1 else 2 end                       -- 奇数排第1，偶数排第2
from travel_route tr
join (
    select s.*,
           row_number() over (partition by s.city order by s.popularity desc, s.score desc, s.id asc) rn
    from scenic_spot s
    where s.city in ('上海','成都','西安','厦门','桂林','重庆')
) ranked on ranked.city = tr.city and ranked.rn <= case when tr.days = 2 then 4 else 5 end
join scenic_spot s on s.id = ranked.id
where tr.city in ('上海','成都','西安','厦门','桂林','重庆');
