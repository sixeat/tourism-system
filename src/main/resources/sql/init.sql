-- ------------------------------------------------------------
--  数据库创建与选择
-- ------------------------------------------------------------
-- 创建数据库 tourism_system，如果不存在则创建；
-- 默认字符集设为 utf8mb4，支持完整的 Unicode 字符（包括 Emoji 和生僻汉字），
-- 排序规则使用默认 utf8mb4_general_ci，兼顾性能和中文兼容性。
create database if not exists tourism_system default character set utf8mb4;

-- 切换到 tourism_system 数据库，后续所有操作在此数据库上下文中执行。
use tourism_system;

-- ============================================================
--  第一阶段：核心基础表结构设计（DDL）
--  设计原则：
--  1. 统一使用 bigint 自增主键（auto_increment），保证主键单调递增且全局唯一；
--  2. 所有金额字段使用 decimal(10,2)，避免浮点数精度丢失；
--  3. 所有状态字段使用 int 或 varchar 枚举，默认赋予合理初始值；
--  4. 时间戳字段使用 datetime default current_timestamp，自动记录创建时间；
--  5. 外键关系通过业务代码维护，不在数据库层设物理外键，提升写入性能和灵活度。
-- ============================================================

-- ------------------------------------------------------------
--  表：scenic_spot（景点信息表）
--  用途：存储全国各城市的景点/景区基础信息，是系统的核心主数据之一。
--  设计说明：
--  - scenic_name + city 组合可唯一标识一个景点；
--  - longitude/latitude 使用 decimal(10,6) 存储 WGS-84 坐标，精确到约 0.1 米；
--  - score 使用 decimal(3,1) 存储 0.0~5.0 或 0.0~10.0 的评分；
--  - tags 为逗号分隔的标签字符串，便于前端展示和搜索过滤。
-- ------------------------------------------------------------
create table if not exists scenic_spot (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    scenic_name varchar(100) not null,                   -- 景点名称，如"西湖风景名胜区"
    city varchar(50) not null,                           -- 所属城市，如"杭州"
    category varchar(50),                                 -- 分类：自然风光、人文古迹、城市地标等
    description text,                                     -- 景点详细描述，支持长文本
    price decimal(10, 2),                                 -- 门票参考价格（元），decimal 避免精度丢失
    score decimal(3, 1),                                  -- 用户评分，如 4.8
    popularity int,                                       -- 热度指数，用于排序和推荐
    tags varchar(255),                                    -- 标签集合，逗号分隔，如"自然,摄影,休闲"
    longitude decimal(10, 6),                             -- 经度，WGS-84 坐标系
    latitude decimal(10, 6)                              -- 纬度，WGS-84 坐标系
);

-- ------------------------------------------------------------
--  表：sys_user（系统用户表）
--  用途：存储注册用户信息，包括普通游客和管理员。
--  设计说明：
--  - username 设为 unique，作为登录账号；
--  - password 存储明文（演示环境），生产环境必须存储 bcrypt/argon2 哈希值；
--  - role 字段区分 USER 和 ADMIN 两种角色；
--  - status 字段控制账号状态：1 正常，0 禁用。
-- ------------------------------------------------------------
create table if not exists sys_user (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    username varchar(50) not null unique,              -- 登录用户名，全局唯一约束
    password varchar(100) not null,                    -- 登录密码（演示环境使用明文，生产必须加密）
    phone varchar(20),                                   -- 手机号，用于联系和短信通知
    email varchar(100),                                  -- 电子邮箱
    role varchar(20) default 'USER',                    -- 角色：USER（普通用户）/ ADMIN（管理员）
    status int default 1,                              -- 账号状态：1-正常，0-禁用
    create_time datetime default current_timestamp     -- 注册时间，自动填充当前时间
);

-- ------------------------------------------------------------
--  表：hotel（酒店信息表）
--  用途：存储各城市的酒店/民宿基础信息，与景点形成旅游产品矩阵。
--  设计说明：
--  - level 字段记录酒店星级或类型（如五星级、精品民宿）；
--  - status 字段支持酒店上下架控制，1 为可预订，0 为下架。
-- ------------------------------------------------------------
create table if not exists hotel (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    hotel_name varchar(100) not null,                    -- 酒店名称
    city varchar(50) not null,                         -- 所属城市
    address varchar(255),                                -- 详细地址
    level varchar(20),                                   -- 酒店级别：五星级、四星级、精品民宿等
    description text,                                   -- 酒店描述，设施、特色等
    status int default 1                               -- 状态：1-正常营业，0-下架
);

-- ------------------------------------------------------------
--  表：hotel_room（酒店房型表）
--  用途：存储酒店下各房型的库存、价格、状态信息，与 hotel 形成一对多关系。
--  设计说明：
--  - hotel_id 逻辑关联 hotel 表，不建物理外键以保证灵活性；
--  - stock 记录可预订房间数，支持库存控制；
--  - room_status 记录房态：AVAILABLE（可预订）、SOLD_OUT（售罄）、MAINTENANCE（维护中）。
-- ------------------------------------------------------------
create table if not exists hotel_room (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    hotel_id bigint not null,                          -- 关联酒店 ID（逻辑外键）
    room_type varchar(50) not null,                    -- 房型名称：标准间、湖景房、套房等
    price decimal(10, 2) not null,                     -- 房型单价（元/晚）
    stock int default 0,                               -- 可预订库存数量
    room_status varchar(20) default 'AVAILABLE'        -- 房态：AVAILABLE / SOLD_OUT / MAINTENANCE
);

-- ------------------------------------------------------------
--  表：ticket（门票信息表）
--  用途：存储各景点的门票产品信息，支持按日期销售。
--  设计说明：
--  - scenic_id 逻辑关联 scenic_spot 表；
--  - available_date 支持指定门票的有效日期，便于未来扩展为预约制门票；
--  - stock 控制每日门票库存，防止超卖。
-- ------------------------------------------------------------
create table if not exists ticket (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    scenic_id bigint not null,                         -- 关联景点 ID（逻辑外键）
    ticket_name varchar(100) not null,                 -- 门票名称，如"西湖成人票"
    price decimal(10, 2) not null,                     -- 门票售价（元）
    stock int default 0,                               -- 可售库存数量
    available_date date                                  -- 门票可用日期，支持预约制
);

-- ------------------------------------------------------------
--  表：travel_route（旅游路线表）
--  用途：存储系统推荐的旅游线路/攻略，将多个景点串联成行程。
--  设计说明：
--  - days 字段表示行程天数，如 2 日、3 日；
--  - budget 字段为预算参考金额，帮助用户快速筛选；
--  - theme 字段标记路线主题：城市精华、美食文化、经济实惠等。
-- ------------------------------------------------------------
create table if not exists travel_route (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    route_name varchar(100) not null,                  -- 路线名称，如"杭州三日精华游"
    city varchar(50) not null,                         -- 所属城市
    days int not null,                                   -- 行程天数
    budget decimal(10, 2),                              -- 参考预算（元）
    theme varchar(50),                                   -- 主题标签：城市精华、历史文化、经济实惠等
    route_desc text                                      -- 路线详细描述
);

-- ------------------------------------------------------------
--  表：route_spot（路线-景点关联表）
--  用途：实现 travel_route 与 scenic_spot 的多对多关联，
--       同时记录每个景点在路线中的日程安排和排序。
--  设计说明：
--  - route_id 关联 travel_route；
--  - scenic_id 关联 scenic_spot；
--  - day_no 表示该景点安排在第几天；
--  - sort_no 表示在同一天内的游览顺序（升序排列）。
-- ------------------------------------------------------------
create table if not exists route_spot (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    route_id bigint not null,                          -- 关联路线 ID（逻辑外键）
    scenic_id bigint not null,                         -- 关联景点 ID（逻辑外键）
    day_no int not null,                                 -- 第几天（1-based）
    sort_no int not null                                 -- 当天内的排序号（升序）
);

-- ------------------------------------------------------------
--  表：hotel_order（酒店订单表）
--  用途：存储用户的酒店预订订单，记录入住/退房日期、金额、状态等。
--  设计说明：
--  - user_id 关联 sys_user，hotel_id 关联 hotel，room_id 关联 hotel_room；
--  - check_in_date / check_out_date 支持跨天预订；
--  - total_amount 为订单总金额，decimal 确保金额精度；
--  - order_status 记录订单生命周期：CREATED（已创建）、PAID（已支付）、CONFIRMED（已确认）、CANCELLED（已取消）等。
-- ------------------------------------------------------------
create table if not exists hotel_order (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    user_id bigint not null,                           -- 下单用户 ID（逻辑外键）
    hotel_id bigint not null,                          -- 预订酒店 ID（逻辑外键）
    room_id bigint not null,                           -- 预订房型 ID（逻辑外键）
    check_in_date date not null,                       -- 入住日期
    check_out_date date not null,                      -- 退房日期
    total_amount decimal(10, 2) not null,             -- 订单总金额（元）
    order_status varchar(20) default 'CREATED',      -- 订单状态：CREATED / PAID / CONFIRMED / CANCELLED
    create_time datetime default current_timestamp    -- 订单创建时间
);

-- ------------------------------------------------------------
--  表：ticket_order（门票订单表）
--  用途：存储用户的景点门票购买订单。
--  设计说明：
--  - ticket_id 关联 ticket 表；
--  - visit_date 指定实际参观日期；
--  - quantity 购买数量，total_amount = price * quantity；
--  - 状态流转与 hotel_order 一致，便于统一订单管理。
-- ------------------------------------------------------------
create table if not exists ticket_order (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    user_id bigint not null,                           -- 下单用户 ID（逻辑外键）
    ticket_id bigint not null,                         -- 购买门票 ID（逻辑外键）
    visit_date date not null,                          -- 参观日期
    quantity int not null,                               -- 购买数量
    total_amount decimal(10, 2) not null,             -- 订单总金额（元）
    order_status varchar(20) default 'CREATED',      -- 订单状态：CREATED / PAID / CONFIRMED / CANCELLED
    create_time datetime default current_timestamp    -- 订单创建时间
);

-- ------------------------------------------------------------
--  表：user_favorite（用户收藏表）
--  用途：存储用户的收藏记录，支持收藏景点、酒店、路线等多种类型。
--  设计说明：
--  - favorite_type 标记收藏类型：SCENIC（景点）、HOTEL（酒店）、ROUTE（路线）等；
--  - target_id 为被收藏对象的唯一标识，与 favorite_type 组合使用；
--  - 复合唯一索引 uk_user_favorite_target 确保同一用户不会重复收藏同一对象。
-- ------------------------------------------------------------
create table if not exists user_favorite (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    user_id bigint not null,                           -- 收藏用户 ID（逻辑外键）
    scenic_id bigint,                                    -- 景点 ID（冗余字段，兼容旧逻辑）
    favorite_type varchar(20) default 'SCENIC',       -- 收藏类型：SCENIC / HOTEL / ROUTE 等
    target_id varchar(100),                             -- 被收藏对象的外部标识
    title varchar(100),                                  -- 收藏标题（快照，避免关联对象改名后显示异常）
    description varchar(255),                          -- 收藏描述
    create_time datetime default current_timestamp,    -- 收藏时间
    unique key uk_user_favorite_target (user_id, favorite_type, target_id) -- 复合唯一约束：防重复收藏
);

-- ------------------------------------------------------------
--  表：order_review（订单评价表）
--  用途：存储用户对酒店订单或门票订单的评价/评分。
--  设计说明：
--  - order_type 区分 HOTEL 和 TICKET 两种订单类型；
--  - order_id 关联对应订单表的 ID；
--  - score 为 1~5 的整数评分；
--  - content 为文本评价内容；
--  - 复合唯一索引防止同一用户对同一订单重复评价。
-- ------------------------------------------------------------
create table if not exists order_review (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    user_id bigint not null,                           -- 评价用户 ID（逻辑外键）
    order_type varchar(20) not null,                   -- 订单类型：HOTEL / TICKET
    order_id bigint not null,                          -- 关联订单 ID（逻辑外键）
    score int not null,                                  -- 评分：1~5 星
    content varchar(500) not null,                     -- 评价内容
    create_time datetime default current_timestamp,    -- 评价时间
    unique key uk_order_review_user_order (user_id, order_type, order_id) -- 复合唯一约束：一订单一评价
);

-- ------------------------------------------------------------
--  表：map_point（地图点位表）
--  用途：存储地图上的 POI（Point of Interest）数据，
--       支持景点、酒店、美食、机场、火车站等多种类型，用于地图可视化展示。
--  设计说明：
--  - point_type 标记点位类型：景点、酒店、美食、机场、火车站；
--  - sort_no 用于前端列表排序，值越小越靠前；
--  - status 控制点位是否在前端展示。
-- ------------------------------------------------------------
create table if not exists map_point (
    id bigint primary key auto_increment,               -- 主键，自增唯一标识
    point_name varchar(100) not null,                  -- 点位名称
    point_type varchar(30) not null,                 -- 点位类型：景点、酒店、美食、机场、火车站
    city varchar(50) not null,                         -- 所属城市
    address varchar(255),                                -- 详细地址
    description varchar(500),                            -- 点位描述
    price decimal(10, 2),                                 -- 参考价格（元）
    score decimal(3, 1),                                  -- 用户评分
    tags varchar(255),                                    -- 标签集合
    longitude decimal(10, 6),                             -- 经度
    latitude decimal(10, 6),                              -- 纬度
    sort_no int default 0,                              -- 排序号，值越小越靠前
    status int default 1                               -- 展示状态：1-展示，0-隐藏
);

